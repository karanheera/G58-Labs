# Variable: employee's monthly salary
salary = 60000

# Variable: tax rate (10%)
tax_rate = 10 / 100

# Processing: calculate tax amount
tax_amount = salary * tax_rate

# Processing: calculate salary after tax
salary_after_tax = salary - tax_amount

# Output: display salary details
print("Monthly Salary:", salary)
print("Tax Amount:", tax_amount)
print("Salary After Tax:", salary_after_tax)

